def process_gmp_data(df_endlineboth):

    # Copy and filter
    df = df_endlineboth.copy()
    df = df[['Wasting', 'Stunting', 'Underweight']]
    df = df.dropna(subset=['Wasting', 'Stunting', 'Underweight'])

    # Clean values
    for col in ['Wasting', 'Stunting', 'Underweight']:
        df[col] = df[col].astype(str).str.strip()

    # Melt for analysis
    df_melted = df.melt(var_name="Severity", value_name="Status")

    # Pivot table
    pivot = df_melted.pivot_table(index="Status", columns="Severity", aggfunc="size", fill_value=0)

    # Combine 'Moderate' + 'Severe' as 'Not Normal'
    combined = pivot.loc[['Moderate', 'Severe']].groupby(lambda x: 'Not Normal').sum()

    # Combine with 'Normal'
    pivot_combined = pd.concat([
        pivot.loc[['Normal']],
        combined
    ]).reset_index().rename(columns={'index': 'Status'})

    # Prepare counts and percentages
    df_counts = pivot_combined.copy()
    df_percentages = pivot_combined.copy()
    column_totals = df_counts.drop(columns=['Status']).sum()

    for col in ['Stunting', 'Underweight', 'Wasting']:
        df_counts[col] = df_counts[col].apply(lambda x: f"(n={x})")
        df_percentages[col] = ((df_percentages[col] / column_totals[col]) * 100).round().astype(int).astype(str) + '%'

    # Merge counts and percentages
    final_df = df_counts[['Status']].copy()
    for col in ['Stunting', 'Underweight', 'Wasting']:
        final_df[f"{col} (n)"] = df_counts[col]
        final_df[f"{col} (%)"] = df_percentages[col]

    return final_df
