#Stunting Results: Contingency Table of Age Group by Center Type
def stunting_contingency_age_center(df):

    df_copy = df.copy()

    # Total population (ALL children)
    total_pop = pd.pivot_table(
        df_copy,
        index='age_group',
        columns='center_type',
        aggfunc='size',
        fill_value=0
    )

    # Moderate + Severe only
    stunted = df_copy[df_copy['Stunting'].isin(['Moderate', 'Severe'])]

    stunted_counts = pd.pivot_table(
        stunted,
        index='age_group',
        columns='center_type',
        aggfunc='size',
        fill_value=0
    )

    # Align both tables
    stunted_counts = stunted_counts.reindex_like(total_pop).fillna(0)

    # Calculate prevalence %
    pct = (stunted_counts / total_pop) * 100
    pct = pct.round(1)

    # Combine counts + %
    final = total_pop.copy().astype(str)

    for col in total_pop.columns:
        final[col] = (
            stunted_counts[col].astype(int).astype(str)
            + ' (' +
            pct[col].astype(str) + '%)'
        )

    # Row total (per age group)
    final['Total'] = (
        stunted_counts.sum(axis=1).round(0).astype(int).astype(str)
        + ' (' +
        ((stunted_counts.sum(axis=1) / total_pop.sum(axis=1)) * 100)
        .round(1).astype(str) + '%)'
    )

    # OVERALL ROW

    overall_stunted = stunted_counts.sum(axis=0)
    overall_total = total_pop.sum(axis=0)
    overall_pct = (overall_stunted / overall_total * 100).round(1)

    overall_row = (
        overall_stunted.round(0).astype(int).astype(str)
        + ' (' +
        overall_pct.astype(str) + '%)'
    )

    # Add total column for overall
    overall_row['Total'] = (
        str(int(stunted_counts.values.sum()))
        + ' (' +
        str(round((stunted_counts.values.sum() / total_pop.values.sum()) * 100, 1))
        + '%)'
    )

    overall_row.name = 'Overall'

    # Append
    final = pd.concat([final, overall_row.to_frame().T])

    return final


