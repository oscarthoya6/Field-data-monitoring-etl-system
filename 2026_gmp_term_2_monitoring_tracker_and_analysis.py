#Wasting summary table by county

def process_gmp_wasting_summary(df):
    # Keep only relevant columns
    df = df[['county', 'Wasting']].copy()

    # Create pivot table
    pivot_summary = df.pivot_table(
        index="county", columns="Wasting", aggfunc="size", fill_value=0
    )

    # Ensure all required columns exist
    for col in ["Normal", "Moderate", "Severe"]:
        if col not in pivot_summary:
            pivot_summary[col] = 0

    # Compute # Moderate to Severe
    pivot_summary["# Moderate to Severe"] = pivot_summary["Moderate"] + pivot_summary["Severe"]

    # Compute total per county
    pivot_summary["Total"] = pivot_summary["Normal"] + pivot_summary["# Moderate to Severe"]

    # Compute percentages (fixed rounding)
    pivot_summary["% Moderate to Severe"] = (
        (pivot_summary["# Moderate to Severe"] / pivot_summary["Total"]) * 100
    ).fillna(0).round(0).astype(int).astype(str) + "%"

    # Format columns to display "count (percentage%)"
    pivot_summary["# Normal"] = pivot_summary["Normal"].astype(str)
    pivot_summary["# Moderate to Severe"] = pivot_summary["# Moderate to Severe"].astype(str)

    # Select final columns
    final_df = pivot_summary[["# Normal", "# Moderate to Severe", "% Moderate to Severe"]].reset_index()

    # Add overall row
    overall = df["Wasting"].count()
    normal_total = df[df["Wasting"] == "Normal"].shape[0]
    mod_severe_total = df[df["Wasting"].isin(["Moderate", "Severe"])].shape[0]

    overall_mod_severe_pct = str(
        round((mod_severe_total / overall) * 100, 0)
    ).split('.')[0] + "%"

    overall_row = pd.DataFrame({
        "county": ["Overall"],
        "# Normal": [str(normal_total)],
        "# Moderate to Severe": [str(mod_severe_total)],
        "% Moderate to Severe": [overall_mod_severe_pct]
    })

    # Append Overall row
    final_df = pd.concat([final_df, overall_row], ignore_index=True)

    return final_df

