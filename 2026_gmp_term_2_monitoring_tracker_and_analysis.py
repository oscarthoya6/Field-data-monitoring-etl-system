def compute_WHO_anthro_zscores(df):
    """
    Computes WHO child growth standard Z-scores using the anthro package in R.

    Parameters:
        df (pd.DataFrame): DataFrame with required columns:
            ['SEX', 'MONTHS', 'WEIGHT', 'HEIGHT', 'MUAC', 'EDEMA']

    Returns:
        pd.DataFrame: Input DataFrame with added Z-score columns.
    """
    required_columns = ['SEX', 'MONTHS', 'WEIGHT', 'HEIGHT', 'MUAC', 'EDEMA']

    for col in required_columns:
        if col not in df.columns:
            raise ValueError(f"Missing required column: {col}")

    # Convert columns to R vectors
    r_sex = ro.FloatVector(df['SEX'])
    r_months = ro.FloatVector(df['MONTHS'])
    r_weight = ro.FloatVector(df['WEIGHT'])
    r_height = ro.FloatVector(df['HEIGHT'])
    r_muac = ro.FloatVector(df['MUAC'])
    r_edema = ro.StrVector(df['EDEMA'].astype(str))

    # Compute Z-scores
    r_result = anthro.anthro_zscores(
        sex=r_sex,
        age=r_months,
        is_age_in_month=True,
        weight=r_weight,
        lenhei=r_height,
        measure="l",
        armc=r_muac,
        oedema=r_edema
    )

    # Convert r_data to pandas_df
    with localconverter(ro.default_converter + pandas2ri.converter):
        df_zscores = ro.conversion.rpy2py(r_result)

    # recreate the pandas dataframe
    df_final = pd.concat([df.reset_index(drop=True), df_zscores.reset_index(drop=True)], axis=1)

    return df_final

