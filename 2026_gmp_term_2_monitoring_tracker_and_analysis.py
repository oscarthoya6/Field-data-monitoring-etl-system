def extract_kobo_complete(api_key, endpoint, form_id, debug=True):
    url = f"{endpoint}/assets/{form_id}/data.json"

    headers = {
        "Authorization": f"Token {api_key}"
    }

    all_results = []
    next_url = url
    total_count = None

    while next_url:
        response = requests.get(next_url, headers=headers, timeout=60)
        response.raise_for_status()
        data = response.json()

        # get total count once
        if total_count is None:
            total_count = data.get("count", 0)
            if debug:
                print(f"Total records in Kobo: {total_count}")

        results = data.get("results", [])
        all_results.extend(results)

        if debug:
            print(f"Fetched {len(results)} rows (running total={len(all_results)})")

        next_url = data.get("next")

        time.sleep(0.3)  # avoid server overload

    # convert to dataframe
    df = pd.json_normalize(all_results, sep='.')

    # create Kobo-like _index column (starts at 1 like Kobo CSV)
    df.reset_index(inplace=True)
    df.rename(columns={'index': '_index'}, inplace=True)
    df['_index'] = df['_index'] + 1

    # move _index to first column (to match Kobo CSV structure)
    cols = ['_index'] + [c for c in df.columns if c != '_index']
    df = df[cols]

    # remove duplicates safely
    if "_uuid" in df.columns:
        before = len(df)
        df = df.drop_duplicates(subset=["_uuid"])
        after = len(df)
        if debug:
            print(f"Removed {before-after} duplicates")

    if debug:
        print(f"✅ FINAL extracted rows: {len(df)}")

    return df

