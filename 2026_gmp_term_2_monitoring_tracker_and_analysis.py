def extract_data_from_kobotoolbox(api_key, end_point, form_id, debug=True):
    kobo = KoboExtractor(api_key, end_point)
    response = kobo.get_data(asset_uid=form_id)

    kobo_results = response.get('results', [])
    if not kobo_results:
        if debug:
            print("No data found for this form.")
        return pd.DataFrame()

    # Flatten the nested JSON to include all system fields
    df = pd.json_normalize(kobo_results, sep='.')

    # Add Kobo-like _index column
    df.reset_index(inplace=True)
    df.rename(columns={'index': '_index'}, inplace=True)

    # Make sure all Kobo system fields appear, even if missing in some rows
    expected_system_fields = [
        '_index',
        '_id',
        '_uuid',
        '_submission_time',
        '_submitted_by',
        '_version',
        '_validation_status.uid',
        '_validation_status.label',
        '_review_status',
        '_attachments'
    ]

    for col in expected_system_fields:
        if col not in df.columns:
            df[col] = None  # Create the column if missing

    # Reorder columns: system fields first, then survey data
    sys_cols = [c for c in expected_system_fields if c in df.columns]
    other_cols = [c for c in df.columns if c not in sys_cols]
    df = df[sys_cols + other_cols]

    if debug:
        print(f"Extracted {len(df)} records and {len(df.columns)} columns.")
        print("System columns found:", [c for c in sys_cols if c in df.columns])

    return df

