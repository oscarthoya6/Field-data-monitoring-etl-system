"""## 2.3 Stunting, Wasting, Underweight

[WHO Guidelines](https://www.who.int/data/nutrition/nlis/info/malnutrition-in-children)

**For Wasting (WHZ - Weight-for-Height Z-score):**

- < -3 → "Severely Wasted"
- -3 to -2 → "Moderately Wasted"
- ≥ -2 → "Normal"

**For Stunting (HAZ - Height-for-Age Z-score):**

- < -3 → "Severely Stunted"
- -3 to -2 → "Moderately Stunted"
- ≥ -2 → "Normal"

**For Underweight (WAZ - Weight-for-Age Z-score):**

- < -3 → "Severely Underweight"
- -3 to -2 → "Moderately Underweight"
- ≥ -2 → "Normal"

**For Overweight Weight-for-Height Z-score (WHZ/zwfl):**
- Overweight: WHZ > +2
- Obese: WHZ > +3
- Normal: -2 ≤ WHZ ≤ +2
"""

def calculate_nutrition_status(df):
    """
    Computes wasting, stunting, underweight, and overweight classifications
    based on WHO  guidelines.
    """
    df = df.copy()

    def classify_indicator(z_score, moderate_label, severe_label):
      if pd.isna(z_score):
        return pd.NA
      elif z_score < -3:
        return severe_label
      elif z_score < -2:
        return moderate_label
      else:
        return "Normal"


    def classify_overweight(z_score):
      if pd.isna(z_score):
        return pd.NA
      elif z_score > 3:
        return "Obese"
      elif z_score > 2:
        return "Overweight"
      else:
        return "Normal"

    df['Wasting'] = df['zwfl (WHZ-WHO)'].apply(lambda x: classify_indicator(x, "Moderately Wasted", "Severely Wasted"))
    df['Stunting'] = df['zlen (HAZ-WHO)'].apply(lambda x: classify_indicator(x, "Moderately Stunted", "Severely Stunted"))
    df['Underweight'] = df['zwei (WAZ-WHO)'].apply(lambda x: classify_indicator(x, "Moderately Underweight", "Severely Underweight"))
    df['Overweight'] = df['zwfl (WHZ-WHO)'].apply(classify_overweight)

    return df
df_z_scores_binned = calculate_nutrition_status(df_gmp_scored)

