def nomolize_child_data_with_cumulative_index(df, meta_cols, record_path):
    exploded_rows = []
    global_index = 1  # Start counting from 1

    for _, row in df.iterrows():
        repeat_group = row.get(record_path, [])
        if isinstance(repeat_group, list):
            for child in repeat_group:
                combined = {**row[meta_cols].to_dict(), **child}
                combined['_index'] = global_index
                exploded_rows.append(combined)
                global_index += 1

    nomolized_df = pd.DataFrame(exploded_rows)
    return nomolized_df

