# Normalize child gmp data
def normalize_child_data(df,meta_cols,record_path,axis=1):
  # Explode data
  df = df.explode(column=record_path).reset_index(drop=True)
  # pd.json_normalize expects a list of JSON objects not a DataFrame
  df = pd.concat([df[meta_cols], pd.json_normalize(df[record_path])],axis=1)
  return df

